    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtNombre.getText().isEmpty()|| txtNumero.getText().isEmpty()
                ||cmbCantidad.getSelectedIndex()==0 || cmbPileta.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Error al cargar los datos");
            return;
    }
        int numero = Integer.parseInt(txtNumero.getText());
        String nombre = txtNombre.getText();
        int cantDisciplinas = cmbCantidad.getSelectedIndex();
        int accesoPileta = cmbPileta.getSelectedIndex();
        
        Socio oSocio= new Socio(numero, nombre, cantDisciplinas, accesoPileta);
        listaSocios.AgregarSocio(oSocio);
        
        JOptionPane.showMessageDialog(null, "Socio cargado con Ã©xito");
        
        txtNombre.setText("");
        txtNumero.setText("");
        cmbCantidad.setSelectedIndex(0);
        cmbPileta.setSelectedIndex(0);
        
    }//GEN-LAST:event_btnAgregarActionPerformed



